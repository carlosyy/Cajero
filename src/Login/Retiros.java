    private void jbtnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnRetirarActionPerformed
        if (retirar(Integer.parseInt(jtxtMontoRetirar.getText()))) {
            JOptionPane.showMessageDialog(null, "El retiro ha sido exitoso, recoja su dinero por favor");
            setDefaultCloseOperation(this.DISPOSE_ON_CLOSE);
            jlblSaldoDisponible.setText(String.valueOf(Consultas.getInstancia().SaldoDisponible));
            Menu.getInstancia().setSaldo();
        } else {
            JOptionPane.showMessageDialog(null, "Recursos insuficientes, no puede retirar esta cantidad");
        }
    }//GEN-LAST:event_jbtnRetirarActionPerformed

