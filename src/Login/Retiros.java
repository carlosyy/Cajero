    private static boolean retirar(int monto) {
        boolean retiroExitoso = false;
        Consultas c = Consultas.getInstancia();
        if (monto <= c.SaldoDisponible) {
            c.SaldoDisponible -= monto;
            retiroExitoso = true;
        }
        return retiroExitoso;
    }

